package com.example.valparaisoapp.ui.slideshow;

public class FormatFotosAnexasINV {

    public boolean activo;
    public String fechaFotosAnexas;
    public String nombreFotosAnexasINV;
    public String autorFotosAnexas;
    public String obsFotosAnexas;

    public FormatFotosAnexasINV(boolean activo, String fechaFotosAnexas, String nombreFotosAnexasINV, String autorFotosAnexas, String obsFotosAnexas) {
        this.activo = activo;
        this.fechaFotosAnexas = fechaFotosAnexas;
        this.nombreFotosAnexasINV = nombreFotosAnexasINV;
        this.autorFotosAnexas = autorFotosAnexas;
        this.obsFotosAnexas = obsFotosAnexas;
    }
}
